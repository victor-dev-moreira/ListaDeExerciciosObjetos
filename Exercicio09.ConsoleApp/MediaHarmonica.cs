namespace Exercicio09.ConsoleApp;

public class MediaHarmonica
{
    public double media1;
    public double media2;
    public double media3;

    public double CalcularMediaHarmonica()
    {
        media1 = 1 / media1;
        media2 = 1 / media2;
        media3 = 1 / media3;
        double media = 3 / (media1 + media2 + media3);
        return media;
    }
}
